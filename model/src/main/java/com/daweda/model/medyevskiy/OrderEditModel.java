package com.daweda.model.medyevskiy;


public class OrderEditModel {

	private int id;
	private String assetName;
	private String assetNameDesc;
	private String position;
	private int units;
	private String price; //deal reate
	
	
	
	
	public String getAssetNameDesc() {
		return assetNameDesc;
	}
	public void setAssetNameDesc(String assetNameDesc) {
		this.assetNameDesc = assetNameDesc;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public int getUnits() {
		return units;
	}
	public void setUnits(int units) {
		this.units = units;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getAssetName() {
		return assetName;
	}
	public void setAssetName(String assetName) {
		this.assetName = assetName;
	}
	
	
	
}
