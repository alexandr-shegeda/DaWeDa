package com.daweda.model.medyevskiy;

public class GraphUpdateRequestModel {

	private String assetName;
	private long clientLastUpdate;
	public GraphUpdateRequestModel(String assetName, long clientLastUpdate) {
		super();
		this.assetName = assetName;
		this.clientLastUpdate = clientLastUpdate;
	}
	public GraphUpdateRequestModel() {
		super();
	}
	public String getAssetName() {
		return assetName;
	}
	public void setAssetName(String assetName) {
		this.assetName = assetName;
	}
	public long getClientLastUpdate() {
		return clientLastUpdate;
	}
	public void setClientLastUpdate(long clientLastUpdate) {
		this.clientLastUpdate = clientLastUpdate;
	}
	
	
}
